package br.ufc.tabd.model;

public class Roads {
	
	private int id_edge;
	private int id_source;
	private int id_target;
	private double cost_id;
	
	public int getId_edge() {
		return id_edge;
	}
	public void setId_edge(int id_edge) {
		this.id_edge = id_edge;
	}
	public int getId_source() {
		return id_source;
	}
	public void setId_source(int id_source) {
		this.id_source = id_source;
	}
	public int getId_target() {
		return id_target;
	}
	public void setId_target(int id_target) {
		this.id_target = id_target;
	}
	public double getCost_id() {
		return cost_id;
	}
	public void setCost_id(double cost_id) {
		this.cost_id = cost_id;
	}
	
	
	
	
}
